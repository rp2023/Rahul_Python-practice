#checking data types
print(type(10)) #int
print(type(3.14)) #float
print(type(1+3j)) #complex
print(type('Rahul')) #string
print(type([1,2,3])) #list
print(type({'name':'Rahul'})) #Dictionary
print(type({9.8, 3.14 , 2.7})) #set
print(type((9.8, 3.14, 2.7))) #tuple
print(type(3==3)) #bool
print(type(3>=3)) #bool
